package com.company;

public class UserChats {
    String messageName;
    String messageTitle;
    String userName;
    String email;

    public UserChats() {
    }

    public UserChats(String messageName, String messageTitle, String userName, String email) {
        this.messageName = messageName;
        this.messageTitle = messageTitle;
        this.userName = userName;
        this.email = email;
    }
}
